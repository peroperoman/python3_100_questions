def cal(num1, num2):
    print(f'{num1} + {num2} = {num1 + num2}')
    print(f'{num1} - {num2} = {num1 - num2}')
    print(f'{num1} x {num2} = {num1 * num2}')
    print(f'{num1} ÷ {num2} = {num1 // num2} 余り {num1 % num2}')
    print(f'{num1} ÷ {num2} = {num1 / num2}')

if __name__ == '__main__':
    cal(7, 3)
